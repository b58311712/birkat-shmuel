-- מטבח החסד - מיגרציה 40: התניית תוספת בתשלום בהזמנת מאכל מסוים
-- =============================================================================
-- עד כה כל תוספת פעילה (extras) הוצגה לכל לקוח בכל הזמנה.
-- יש תוספות שרלוונטיות רק כשהוזמן מאכל מסוים - למשל רוטב שמוצע רק למי
-- שהזמין דגים, או לחם מיוחד שנלווה למרק.
--
-- מודל: טבלת קישור (תוספת x מאכל) עם סמנטיקת "או":
--   אין שורות לתוספת  = תוספת ללא התניה, מוצגת תמיד (התנהגות קיימת).
--   יש שורה אחת ויותר = התוספת מוצגת רק אם נבחר לפחות אחד מהמאכלים המקושרים
--                        (בכל סעודה שהיא) באותה הזמנה.
-- טבלה נפרדת ולא עמודה בודדת ב-extras, כדי לאפשר לקשור כמה מאכלים
-- (למשל כל סוגי הדגים) בלי לשכפל את כרטיס התוספת.
-- =============================================================================

create table if not exists extra_meal_requirements (
  extra_id   uuid not null references extras(id) on delete cascade,
  meal_id    uuid not null references meals(id) on delete cascade,
  created_at timestamptz not null default now(),
  primary key (extra_id, meal_id)
);

comment on table extra_meal_requirements is
  'התניית תוספת בתשלום בהזמנת מאכל: התוספת מוצגת רק אם נבחר לפחות אחד מהמאכלים הללו. אין שורות = ללא התניה (סעיף 14).';

create index if not exists idx_extra_meal_requirements_meal on extra_meal_requirements(meal_id);
