-- =============================================================================
-- מטבח החסד — מיגרציה 34: מיילי רישום לקוח חדש (סעיף 7 + 18)
-- =============================================================================
-- כשלקוח שולח בקשת רישום:
--   1) התראה נשלחת לתיבת המשרד (new_registration_manager_alert).
-- כשהמנהל מטפל בבקשה:
--   2) אישור -> מייל חוזר ללקוח (registration_approved).
--   3) דחייה -> מייל חוזר ללקוח עם הסיבה (registration_rejected).
--
-- שלושת הנוסחים ניתנים לעריכה ע"י המנהל במסך /admin/email, ככל שאר הנוסחים.
-- הערה: גופי הטקסט עטופים ב-dollar-quoting ($body$...$body$) כדי שתווים בעברית
-- (מרכאות, גרש, RTL) לא ישברו את המחרוזת — לקח שנצרב במיגרציה 19.
-- =============================================================================

insert into email_templates (code, subject, body, is_active)
values
  (
    'new_registration_manager_alert',
    $subj$בקשת רישום לקוח חדש — מטבח החסד$subj$,
    $body$התקבלה בקשת רישום חדשה הממתינה לאישור.

שם מלא: {full_name}
טלפון: {phone}
אימייל: {email}
כתובת: {address}

נא להיכנס למסך הרישומים במערכת כדי לאשר או לדחות את הבקשה.

מטבח החסד$body$,
    true
  ),
  (
    'registration_approved',
    $subj$בקשת הרישום אושרה — מטבח החסד$subj$,
    $body$שלום {full_name},

שמחים לבשר שבקשת הרישום שלך למערכת מטבח החסד אושרה.
מעתה ניתן להיכנס למערכת ולהזמין לשבתות.

בברכה,
מטבח החסד$body$,
    true
  ),
  (
    'registration_rejected',
    $subj$בקשת הרישום לא אושרה — מטבח החסד$subj$,
    $body$שלום {full_name},

לצערנו בקשת הרישום שלך למערכת מטבח החסד לא אושרה.
סיבה: {reject_reason}

לבירורים נוספים ניתן לפנות למשרד.

בברכה,
מטבח החסד$body$,
    true
  )
on conflict (code) do nothing;
