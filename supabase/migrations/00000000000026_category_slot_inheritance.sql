-- =============================================================================
-- מטבח החסד — מיגרציה 26: ירושת מאכלים מסעודת-אב (סעיף סלטים בוקר)
-- =============================================================================
-- דרישה: בקטגוריית הסלטים ניתן לבחור עד 5 סלטים בליל שבת; בשבת בבוקר הלקוח
-- מקבל אוטומטית את אותם הסלטים שנבחרו בלילה, ובנוסף רשאי להוסיף עד 2 סלטים.
--
-- המנגנון דינמי ופר-קטגוריה (לא מקובע לסלטים בלבד):
--   inherit_from_slot_id — הסעודה שממנה הקטגוריה יורשת (ליל שבת).
--   extra_allowed        — כמה מאכלים מותר להוסיף בבוקר מעבר לירושה.
--
-- פרשנות: בכל סעודה שאיננה inherit_from_slot_id, מאכלי הקטגוריה שנבחרו
-- בסעודת-האב מסומנים אוטומטית (ירושה), וניתן להוסיף עד extra_allowed נוספים.
-- בסעודת-האב עצמה נשמר כלל max_allowed הרגיל (5 סלטים).
--
-- אין שינוי בשרת/מטבח/תמחור: הירושה מחומרלת בצד הלקוח כפריטי בוקר אמיתיים,
-- כך שכל סעודה נשלחת עם המאכלים שלה והמטבח מכין כמות נפרדת לבוקר.
-- =============================================================================

alter table categories
  add column inherit_from_slot_id uuid references meal_slots(id),
  add column extra_allowed         integer;

comment on column categories.inherit_from_slot_id is
  'הסעודה שהקטגוריה יורשת ממנה מאכלים. בסעודה אחרת, המאכלים שנבחרו בסעודה זו מסומנים אוטומטית (סלטים: ליל שבת)';
comment on column categories.extra_allowed is
  'כמה מאכלים מותר להוסיף בסעודה היורשת מעבר למאכלים הירושים (סלטים בבוקר: 2)';

-- מקסימום התוספת אינו יכול להיות שלילי.
alter table categories
  add constraint chk_categories_extra_allowed
    check (extra_allowed is null or extra_allowed >= 0);
