-- =============================================================================
-- מטבח החסד — מיגרציה 17: חלוקת מנות בין מאכלים באותה קטגוריה
-- =============================================================================
-- בקטגוריות מסוימות (דגים, מנה עיקרית) הלקוח בוחר יותר מסוג אחד, ורוצה לקבוע
-- כמה מנות מכל סוג. סך הכמויות בקטגוריה כזו חייב להיות שווה למספר המנות של
-- הסעודה. עד כה כל מאכל שנבחר "צרך" את כל מנות הסעודה, כך שבחירת שני סוגים
-- גרמה למטבח להכין כפול (50 + 50 במקום 50 מחולק).
--
--   - categories.requires_portion_split — דגל: הקטגוריה מחלקת את מנות הסעודה
--     בין המאכלים שנבחרו בה (הסכום חייב להשתוות למנות הסעודה).
--   - order_meals.portions — כמות המנות של המאכל הספציפי. NULL = התנהגות ישנה
--     ("כל מנות הסעודה"), כך שהזמנות קיימות נשארות תקינות ותואמות אחורה.
-- =============================================================================

alter table categories
  add column if not exists requires_portion_split boolean not null default false;

comment on column categories.requires_portion_split is
  'true = מנות הסעודה מתחלקות בין המאכלים שנבחרו בקטגוריה; סך הכמויות = מנות הסעודה (סעיף 13).';

alter table order_meals
  add column if not exists portions integer
    check (portions is null or portions > 0);

comment on column order_meals.portions is
  'כמות המנות של המאכל הספציפי (בקטגוריות עם חלוקת מנות). NULL = כל מנות הסעודה (התנהגות ישנה).';

-- ברירת מחדל: דגים ומנה עיקרית מחלקים מנות בין הסוגים שנבחרו.
update categories
set requires_portion_split = true
where name in ('דגים', 'מנה עיקרית');
