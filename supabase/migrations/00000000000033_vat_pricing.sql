-- =============================================================================
-- מטבח החסד - מיגרציה 33: מע"מ במחירי מלאי
-- =============================================================================
-- כל המחירים השמורים במערכת (מחיר קנייה אחרון בכרטיס המוצר, מחיר לספק,
-- מחיר משוער/בפועל בשורות הזמנת רכש) נשמרים מעתה כ**מחיר לפני מע"מ** (מחיר בסיס).
-- המחיר "כולל מע"מ" הוא נגזרת שמחושבת תמיד בזמן התצוגה, ולעולם אינו נשמר.
--
-- שלושה מרכיבים:
--   1. vat_rate - שיעור מע"מ גלובלי אחד בהגדרות המערכת. שינוי המע"מ = עדכון ערך
--      אחד, וכל התצוגות מתעדכנות מיד, בלי לגעת באף מחיר שמור.
--   2. inventory_items.vat_exempt - פריט פטור ממע"מ (פירות/ירקות טריים). אצל פריט
--      פטור המחיר הסופי = מחיר הבסיס, בלי תוספת מע"מ.
--   3. suppliers.default_price_includes_vat - האם הספק נוקב מחירים בחשבונית כולל
--      מע"מ. משמש כברירת מחדל למתג "לפני/כולל" בהזנת מחיר (נורמול קורה בקוד).
-- =============================================================================

-- ----------------------------------------------------------------------------
-- 1. שיעור מע"מ גלובלי (system_settings) - ברירת מחדל 18%
-- ----------------------------------------------------------------------------
insert into system_settings (key, value, description) values
  ('vat_rate', '18', 'שיעור מע"מ באחוזים (משמש לחישוב מחיר כולל מע"מ מתוך מחיר בסיס)')
on conflict (key) do nothing;

-- ----------------------------------------------------------------------------
-- 2. פטור ממע"מ פר-פריט (פירות/ירקות טריים)
-- ----------------------------------------------------------------------------
alter table inventory_items
  add column vat_exempt boolean not null default false;

comment on column inventory_items.vat_exempt is
  'true = פריט פטור ממע"מ (פירות/ירקות טריים); המחיר הסופי = מחיר הבסיס בלי תוספת מע"מ';

-- ----------------------------------------------------------------------------
-- 3. ברירת מחדל של אופן נקיבת המחיר אצל הספק (למתג ההזנה)
-- ----------------------------------------------------------------------------
alter table suppliers
  add column default_price_includes_vat boolean not null default false;

comment on column suppliers.default_price_includes_vat is
  'true = הספק נוקב מחירים בחשבונית כולל מע"מ; ברירת מחדל למתג "לפני/כולל" בהזנת מחיר';

-- ----------------------------------------------------------------------------
-- הבהרת סמנטיקה: כל עמודות המחיר נשמרות כמחיר לפני מע"מ (מחיר בסיס)
-- ----------------------------------------------------------------------------
comment on column inventory_items.last_purchase_price is
  'מחיר קנייה אחרון ליחידה - לפני מע"מ (מחיר בסיס)';
comment on column item_suppliers.last_purchase_price is
  'מחיר קנייה אחרון מספק זה - לפני מע"מ (מחיר בסיס)';
comment on column purchase_order_lines.estimated_price is
  'מחיר משוער ליחידה - לפני מע"מ (מחיר בסיס)';
comment on column purchase_order_lines.actual_price is
  'מחיר בפועל ליחידה - לפני מע"מ (מחיר בסיס)';
