-- =============================================================================
-- מטבח החסד - מיגרציה 24: בקשת תפוסת מנות חריגה (מעל 100 / מתחת 50)
-- =============================================================================
-- ברירת המחדל לכל סעודה היא 50–100 מנות. לקוח יכול לבקש כמות חריגה (יותר מ-100
-- או פחות מ-50), וההזמנה תסומן כ"בקשת חריג" הממתינה לאישור מודע של המנהל.
--   • הגבלת המנות החיובית (portions > 0) כבר נאכפת ב-order_meal_slots (מיגרציה 04),
--     ולכן אין תקרה/רצפה קשיחה נוספת - כל כמות חיובית מותרת בחריג.
--   • הדגל נשמר על ראש ההזמנה כדי שהמנהל יראה בבירור שהוא מאשר כמות לא-סטנדרטית.
--   • מוצג במסך פרטי ההזמנה ליד כפתור האישור; מתעדכן גם ביצירה (לקוח) וגם בעריכה (מנהל).
-- =============================================================================

alter table orders
  add column portions_exception_requested boolean not null default false,
  add column portions_exception_note      text;

comment on column orders.portions_exception_requested is
  'true כאשר לפחות סעודה אחת בהזמנה מחוץ לטווח 50–100 (בקשת חריג הדורשת אישור מנהל)';
comment on column orders.portions_exception_note is
  'פירוט הסעודות החורגות והכמויות שהתבקשו - לתצוגה למנהל';

-- אינדקס חלקי לשליפה מהירה של הזמנות עם בקשת חריג פתוחה
create index idx_orders_portions_exception
  on orders (portions_exception_requested)
  where portions_exception_requested;
