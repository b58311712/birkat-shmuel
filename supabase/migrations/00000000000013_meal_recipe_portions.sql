-- =============================================================================
-- מטבח החסד — מיגרציה 13: מספר המנות המקורי של המתכון (recipe_portions)
-- =============================================================================
-- עד כה נשמרה רק כמות-למנה-אחת (recipe_lines.quantity_per_portion), ומספר
-- המנות שהוזן בעורך (למשל "המתכון מספיק ל־40 מנות") שימש לחלוקה בזמן השמירה
-- ואבד לאחר מכן. לכן בפתיחה מחדש המתכון "התאפס" למנה אחת והכמות המקורית
-- המדויקת של המתכון השלם לא הוצגה.
--
-- כאן שומרים את מספר המנות המקורי על המאכל עצמו. הכמות למנה אחת ממשיכה
-- להישמר ב-recipe_lines כרגיל (כך שאר חישובי המטבח/מלאי לא משתנים), ואת
-- הכמות למתכון השלם משחזרים בקריאה: quantity_per_portion × recipe_portions.

alter table meals
  add column if not exists recipe_portions integer not null default 1
    check (recipe_portions > 0);  -- לכמה מנות נכתב המתכון המקורי

comment on column meals.recipe_portions is
  'מספר המנות שהמתכון המקורי נכתב עבורו. כמות-למנה = quantity_per_portion; כמות למתכון השלם = quantity_per_portion × recipe_portions.';
